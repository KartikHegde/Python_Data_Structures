# Python Performance
#
# Unlike other compiled languages, Python code runs immediately after it is written.
# Byte code is a Python-specific representation. Since byte code is not a binary machine instructions,
# it requires more work than CPU instructions.
#
# On the other hand, unlike other interpreters, there is still an internal compile step -
# Python does not need to reanalyze and reparse each source statement repeatedly.
# So, Python code runs at speeds somewhere between those of a traditional compiled language and
# a classic interpreted language.